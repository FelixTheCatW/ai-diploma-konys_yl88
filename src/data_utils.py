"""
Модуль для работы со списками словарей.

Предоставляет функции поиска по полю "name", фильтрации по ключу-значению
и подсчёта количества элементов.
"""


def find_by_name(data, name):
    """
    Ищет элемент в списке по полю "name".

    Аргументы:
        data (list[dict]): Список словарей, каждый из которых должен иметь ключ "name".
        name (str): Искомое имя.

    Возвращает:
        dict | None: Найденный словарь или None, если не найден.
    """
    for item in data:
        if item["name"] == name:
            return item
    return None


def filter_by_value(data, key, value):
    """
    Фильтрует список словарей, оставляя только те,
    у которых значение по заданному ключу совпадает с указанным.

    Аргументы:
        data (list[dict]): Исходный список.
        key (str): Ключ для фильтрации.
        value: Значение для сравнения.

    Возвращает:
        list[dict]: Отфильтрованный список.
    """
    return [d for d in data if d[key] == value]


def count_items(data):
    """
    Возвращает количество элементов в списке.

    Аргументы:
        data (list): Входной список.

    Возвращает:
        int: Длина списка.
    """
    return len(data)
